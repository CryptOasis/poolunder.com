/**
 * Scroll
 */
var navBarHeight = document.getElementById("nav-bar").offsetHeight;
var sections = ["particles-js", "about", "faq", "join"];

/* Highlight menu items while scrolling */
window.onscroll = function() {
	sections.forEach(function(id) {
		var section = document.getElementById(id).getBoundingClientRect();
		var node = document.getElementById(id + "-link").parentNode;

		if (1 < section.bottom - navBarHeight && 1 > section.top - navBarHeight) {
			document.getElementById(id + "-link").style.opacity = 1;
			//node.className = 'active';
		} else {
			document.getElementById(id + "-link").style.opacity = null;
			//node.className = '';
		}		
	});
}

/* Scroll to section on click */
sections.forEach(function(id) {
	$("#" + id + "-link").click(function() {
		$('html, body').animate({
			scrollTop: $("#" + id).offset().top - navBarHeight
		});
	});
});


/**
 * Listen to scroll to change header opacity class
 */
function checkScroll(){
    var startY = $('.navbar').height() * 2; //The point where the navbar changes in px

//	console.log($(window).scrollTop() + " CHECKSCROLL " + startY, );
    if($(window).scrollTop() > startY){
        $('.navbar').addClass("scrolled");
    }else{
        $('.navbar').removeClass("scrolled");
    }
}

if($('.navbar').length > 0){
    $(window).on("scroll load resize", function(){

        checkScroll();
    });
}


